part of 'signup_bloc.dart';

@immutable
abstract class SignupEvent {}

class SignUpDoctorEvent extends SignupEvent {
  final Doctor doctor;
  final String password;
  SignUpDoctorEvent(this.doctor, this.password);
}

class SignUpPatientEvent extends SignupEvent {
  final Patient patient;
  final String password;

  SignUpPatientEvent(this.patient, this.password);
}
